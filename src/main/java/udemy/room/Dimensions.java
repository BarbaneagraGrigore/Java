package udemy.room;

public class Dimensions {
    private int height;
    private int width;
    private int lenght;

    public Dimensions(int height, int width, int lenght) {
        this.height = height;
        this.width = width;
        this.lenght = lenght;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }

    public int getLenght() {
        return lenght;
    }
}
